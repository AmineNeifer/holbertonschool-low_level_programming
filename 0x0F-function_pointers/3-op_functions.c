#include "calc.h"
#include <stdlib.h>
/**
 * op_add - adding two numbers
 * @a: int.
 * @b: int.
 * Return: a + b.
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - comparing two numbers
 * @a: int.
 * @b: int.
 * Return: a - b.
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - multiplying two numbers
 * @a: int.
 * @b: int.
 * Return: a * b.
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - divide a by b
 * @a: int.
 * @b: int.
 * Return: a / b.
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - a modulo b
 * @a: int.
 * @b: int.
 * Return: a % b.
 */
int op_mod(int a, int b)
{
return (a % b);
}
