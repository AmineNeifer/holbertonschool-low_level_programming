#include "holberton.h"
/**
 * init_dog - intitialize a dog information.
 * @d: 
 * @name: its name.
 * @age: its age.
 * @owner: its owner.
 * Return: void.
 */
void init_dog(struct dog *d, char *name, float age, char *owner)
{
if (d == 0)
return;
dog->name = name;
dog->age = age;
dog->owner = owner;
}
